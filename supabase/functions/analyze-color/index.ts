serve(async (req: Request) => {
  try {
    if (!CLAUDE_API_KEY) {
      return new Response(JSON.stringify({ error: "CLAUDE_API_KEY not configured" }), {
        status: 500,
        headers: { "Content-Type": "application/json" }
      })
    }

    // JWT 토큰 검증
    const authHeader = req.headers.get("Authorization")
    if (!authHeader || !authHeader.startsWith("Bearer ")) {
      return new Response(JSON.stringify({ error: "인증이 필요합니다." }), {
        status: 401,
        headers: { "Content-Type": "application/json" }
      })
    }

    const supabaseClient = createClient(SUPABASE_URL, SUPABASE_ANON_KEY, {
      global: { headers: { Authorization: authHeader } }
    })

    const { data: { user }, error: authError } = await supabaseClient.auth.getUser()
    if (authError || !user) {
      return new Response(JSON.stringify({ error: "인증에 실패했습니다. 다시 로그인해주세요." }), {
        status: 401,
        headers: { "Content-Type": "application/json" }
      })
    }

    const body = await req.json()
    const { messages } = body

    if (!messages || !Array.isArray(messages)) {
      return new Response(JSON.stringify({ error: "messages array required" }), {
        status: 400,
        headers: { "Content-Type": "application/json" }
      })
    }

    console.log(`Calling Claude API with ${messages.length} messages`)

    const response = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "x-api-key": CLAUDE_API_KEY,
        "anthropic-version": "2023-06-01"
      },
      body: JSON.stringify({
        model: "claude-sonnet-4-6",
        max_tokens: 4096,
        system: SYSTEM_PROMPT,
        messages: messages
      })
    })

    const data = await response.json()

    if (!response.ok) {
      console.error(`Claude API error: ${response.status}`, JSON.stringify(data))
      return new Response(JSON.stringify({ error: data.error?.message || `Claude API error: ${response.status}` }), {
        status: response.status,
        headers: { "Content-Type": "application/json" }
      })
    }

    console.log("Claude API success")
    return new Response(JSON.stringify(data), {
      headers: { "Content-Type": "application/json" }
    })
  } catch (error) {
    console.error("Edge Function error:", error.message)
    return new Response(JSON.stringify({ error: error.message }), {
      status: 500,
      headers: { "Content-Type": "application/json" }
    })
  }
})
